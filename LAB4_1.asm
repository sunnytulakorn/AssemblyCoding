.MODEL	SMALL
.STACK	100H
.DATA
HOUR	DB	' HOURS $'
MIN	DB	' MINUTES $'
SEC	DB	' SECONDS $'
.CODE
MAIN PROC
	MOV 	AX,@DATA
	MOV 	DS,AX

	CALL	INDEC
	
	XOR	DX,DX
	MOV	BX,3600 ;หารวินาทีและนาที60*60
	DIV	BX
	PUSH	DX

	CALL 	OUTDEC
	MOV	AH,9
	LEA	DX,HOUR
	INT	21H

	POP	AX
	XOR	DX,DX
	MOV	BX,60
	DIV	BX
	PUSH	DX
	
	POP	AX

	CALL	OUTDEC
	MOV	AH,9
	LEA	DX,SEC
	INT	21H

	MOV	AH,4CH
	INT	21H
MAIN	ENDP
INCLUDE INDEC.ASM
INCLUDE	OUTDEC.ASM
	END MAIN

	