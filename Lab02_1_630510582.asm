.MODEL	SMALL
.STACK	100H
.CODE
MAIN	PROC
;display?
	MOV	AH,2
	MOV	DL,'?'
	INT	21H
;input1
	MOV	AH,1
	INT	21H
	MOV	BL,AL
;input2
	MOV	AH,1
	INT	21H
	MOV	CL,AL
;new line
	MOV	AH,2
	MOV	DL,0DH
	INT	21H
	MOV	DL,0AH
	INT	21H
;condition1
	CMP	BL,CL ;if BL >= CL    
	JG	DISPLAY_1
;condition2
	CMP	BL,CL ;if CL >= BL
	JLE	DISPLAY_2
DISPLAY_1:
	MOV	AH,2
	MOV	DL,CL
	INT	21H
	MOV	AH,2
	MOV	DL,BL
	INT	21H
	JMP	END_IF
DISPLAY_2:
	MOV	AH,2
	MOV	DL,BL
	INT	21H
	MOV	AH,2
	MOV	DL,CL
	INT	21H
END_IF:
;return dos
	MOV	AH,4CH
	INT	21H
MAIN	ENDP
	END MAIN
